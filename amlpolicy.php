              </h2>
              <p>Our Customer Approval Policy delineates the criteria and procedures for onboarding new customers and forging business relationships. The policy is underpinned by the following key principles:</p>
              <ul>
                <li>Risk-Based Approach: We employ a risk-based approach to customer evaluation and acceptance, tailoring the level of due diligence to the perceived risk profile of each customer. Greater scrutiny is applied to higher-risk customers.</li>
                <li>Customer Due Diligence (CDD): Integral to our acceptance process is comprehensive customer due diligence. This encompasses identity verification, understanding the nature of the customer's business, and assessing the sources of their funds.</li>
                <li>Enhanced Due Diligence (EDD): For high-risk customers, including politically exposed persons (PEPs) and select business entities, we implement enhanced due diligence measures to delve deeper into their backgrounds and evaluate potential risks associated with the relationship.</li>
                <li>Ongoing Monitoring: Our policy mandates ongoing monitoring of customer relationships to promptly identify any changes in customer behavior or risk profiles throughout the course of the relationship.</li>
